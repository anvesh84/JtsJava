/* Number Functions */

abs() : returns absolute value 

select abs(-12);

power(n,m) : Returns n power m value 

select power(2,3) 

sqrt(n) : Returns the sqrt value 

select sqrt(49)

ceiling() : Returns the greatest integer value. 

select ceiling(12.0000001) 

floor() : returns the smallest integer value 

select floor(12.9999999)

round()  : Used to round the value to the digits specified 

select round(8786.8868,3) 

select round(8786.8868,2) 

select round(8786.8868,1) 

select round(8786.8868,0) 

select round(8786.8868,-1) 

select round(8786.8868,-2) 

select round(8786.8868,-3) 




