-- Aggregate Functions 

sum() : Performs sum on the numeric field specified 

select sum(sal) from Emp;

avg() : Performs avg on the numeric field specified

select avg(sal) from Emp;

max() : Displays max. value on the numeric field 

select max(sal) from Emp;

min() : Displays min. value on the numeric field 

select min(sal) from Emp;

count(*) : Displays total number of records. 

select count(*) FROM EMP;

